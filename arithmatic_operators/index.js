// arithmetic operators = operands (values, variables, etc.)
//                                         operators (+ - * /)
//                                         ex. 11 = x + 5;

/*
    operator precedence
    1. parenthesis ()
    2. exponents
    3. multiplication & division & modulo
    4. addition & subtraction
*/

let students = 30;

console.log(students + 30);
console.log(students - 30);
console.log(students * 30);
console.log(students / 30);
console.log(students ** 2);
console.log(students++);
console.log(students--);
console.log(++students);
console.log(--students);
